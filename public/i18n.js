  const textDictionaries = {
    [CN]: text,
    [FR]: frText,
  };

  const htmlDictionaries = {
    [CN]: html,
    [FR]: frHtml,
  };

  const attrNames = ['placeholder', 'aria-label', 'title'];
  const originalText = new WeakMap();

  function normalize(value) {
    return String(value || '').replace(/\s+/g, ' ').trim();
  }

  function withSameSpace(source, replacement) {
    const leading = source.match(/^\s*/)?.[0] || '';
    const trailing = source.match(/\s*$/)?.[0] || '';
    return leading + replacement + trailing;
  }

  function currentLang() {
    const saved = localStorage.getItem(KEY);
    return LANGS.includes(saved) ? saved : EN;
  }

  function syncToggle(lang) {
    document.querySelectorAll('[data-lang-toggle]').forEach((btn) => {
      btn.querySelectorAll('[data-lang-en]').forEach((el) => el.classList.toggle('on', lang === EN));
      btn.querySelectorAll('[data-lang-cn]').forEach((el) => el.classList.toggle('on', lang === CN));
      btn.querySelectorAll('[data-lang-fr]').forEach((el) => el.classList.toggle('on', lang === FR));
      btn.setAttribute('aria-pressed', String(lang !== EN));
      btn.setAttribute('aria-label', `Current language: ${lang}`);
    });
  }

  function restoreHtml() {
    document.querySelectorAll('[data-i18n-original-html]').forEach((el) => {
      el.innerHTML = el.getAttribute('data-i18n-original-html') || '';
      el.removeAttribute('data-i18n-html-active');
    });
  }

  function applyHtml(lang) {
    const dictionary = htmlDictionaries[lang] || {};
    const selectors = 'h1,h2,h3,h4,h5,h6,p,.page-hero-sub,.lead-copy';
    document.querySelectorAll(selectors).forEach((el) => {
      if (el.closest('script,style,svg,textarea,input,code,pre,[data-i18n-skip]')) return;
      if (el.querySelector('form,input,select,textarea,svg,img')) return;
      const originalHtml = el.getAttribute('data-i18n-original-html');
      const originalTextKey = originalHtml
        ? normalize(originalHtml.replace(/<br\s*\/?>/gi, '').replace(/<[^>]*>/g, ''))
        : '';
      const key = el.getAttribute('data-i18n-key') || originalTextKey || normalize(el.textContent);
      const translated = dictionary[key];
      if (!translated) {
        if (originalHtml && el.hasAttribute('data-i18n-html-active')) {
          el.innerHTML = originalHtml;
          el.removeAttribute('data-i18n-html-active');
        }
        return;
      }
      if (!el.hasAttribute('data-i18n-original-html')) {
        el.setAttribute('data-i18n-original-html', el.innerHTML);
      }
      el.innerHTML = translated;
      el.setAttribute('data-i18n-html-active', 'true');
    });
  }

  function translateTextNodes(lang) {
    const dictionary = textDictionaries[lang] || {};
    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
      acceptNode(node) {
        const parent = node.parentElement;
        if (!parent) return NodeFilter.FILTER_REJECT;
        if (parent.closest('script,style,svg,textarea,input,code,pre,[data-i18n-skip],[data-i18n-html-active]')) {
          return NodeFilter.FILTER_REJECT;
        }
        if (!normalize(node.nodeValue)) return NodeFilter.FILTER_REJECT;
        return NodeFilter.FILTER_ACCEPT;
      },
    });

    const nodes = [];
    while (walker.nextNode()) nodes.push(walker.currentNode);

    nodes.forEach((node) => {
      if (!originalText.has(node)) originalText.set(node, node.nodeValue || '');
      const source = originalText.get(node) || '';
      if (lang === EN) {
        node.nodeValue = source;
        return;
      }
      const key = normalize(source);
      const translated = dictionary[key];
      if (translated) node.nodeValue = withSameSpace(source, translated);
    });
  }

  function translateAttrs(lang) {
    const dictionary = textDictionaries[lang] || {};
    document.querySelectorAll('*').forEach((el) => {
      attrNames.forEach((name) => {
        if (!el.hasAttribute(name)) return;
        const originalName = 'data-i18n-original-' + name;
        if (!el.hasAttribute(originalName)) el.setAttribute(originalName, el.getAttribute(name) || '');
        const source = el.getAttribute(originalName) || '';
        if (lang === EN) {
          el.setAttribute(name, source);
          return;
        }
        const translated = dictionary[normalize(source)];
        if (translated) el.setAttribute(name, translated);
      });
    });
  }

  function applyLanguage(lang) {
    lang = LANGS.includes(lang) ? lang : EN;
    document.documentElement.setAttribute('data-lang', lang);
    document.documentElement.setAttribute('lang', lang === CN ? 'zh-CN' : lang === FR ? 'fr' : 'en');
    if (lang === EN) {
      restoreHtml();
      translateTextNodes(lang);
      translateAttrs(lang);
    } else {
      applyHtml(lang);
      translateTextNodes(lang);
      translateAttrs(lang);
    }
    syncToggle(lang);
    window.dispatchEvent(new CustomEvent('harmony-language-change', { detail: { lang } }));
  }

  function bindToggles() {
    document.querySelectorAll('[data-lang-toggle]').forEach((btn) => {
      btn.addEventListener('click', (event) => {
        const target = event.target;
        const current = currentLang();
        const explicit = target && target.closest
          ? target.closest('[data-lang-en],[data-lang-cn],[data-lang-fr]')
          : null;
        const next = explicit?.hasAttribute('data-lang-cn')
          ? CN
          : explicit?.hasAttribute('data-lang-fr')
            ? FR
            : explicit?.hasAttribute('data-lang-en')
              ? EN
              : LANGS[(LANGS.indexOf(current) + 1) % LANGS.length];
        localStorage.setItem(KEY, next);
        applyLanguage(next);
      });
    });
  }

  function init() {
    bindToggles();
    applyLanguage(currentLang());
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
